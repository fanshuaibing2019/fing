#include<stdio.h>
#include<stdlib.h>
#include<time.h>
int main(int argc,char const*argv[])
{
	srand(time(0));
	int a=rand();
	a%=100+1;
	printf("我们已经准备了一个1-100的数字，猜猜看：");
	int x,n=0;
	scanf("%d",&x);
	while(x!=a){
	if(x>a)
	printf("你猜的数大了，再猜一次：");
	if(x<a)
	printf("你猜的数小了，再猜一次："); 
	n++;
	scanf("%d",&x);
	}
	printf("你猜了%d次就猜对了",n+1);
	return 0;
 } 
